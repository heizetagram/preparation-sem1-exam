package eksamensopgaver.iklassen.speedrun2.opg13eksamen;

public class Room {
    private int width;
    private int length;

    public Room(int width, int length) {
        this.width = width;
        this.length = length;
    }

    public int getWidth() {
        return width;
    }
    public int getLength() {
        return length;
    }
}
